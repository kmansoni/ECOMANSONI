// Вспомогательные функции
export function getCompanyBySlug(slug: string): InsuranceCompanyInfo | undefined {
  return INSURANCE_COMPANIES.find(c => c.slug === slug);
}

export function getCompanyById(id: string): InsuranceCompanyInfo | undefined {
  return INSURANCE_COMPANIES.find(c => c.id === id);
}

export function getCompaniesByCategory(category: string): InsuranceCompanyInfo[] {
  return INSURANCE_COMPANIES.filter(c => c.categories.includes(category));
}

export function getPartnerCompanies(): InsuranceCompanyInfo[] {
  return INSURANCE_COMPANIES.filter(c => c.isPartner);
}

export function getCompaniesWithApi(): InsuranceCompanyInfo[] {
  return INSURANCE_COMPANIES.filter(c => c.apiAvailable && c.apiType !== 'none');
}
